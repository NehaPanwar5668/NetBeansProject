            void generateUPIQRCode(){
                try{
                    String upi = "7819895972@axl";
                    String amount = jtxtFamount.getText();
                    String qrdata = "upi://pay?pa=" + upi + "&pn=CollegeFees" + "&am=" + amount +"&cu=INR";
                    
                    BitMatrix matrix = new MultiFormatWriter().encode(qrdata, BarcodeFormat.QR_CODE, 250, 250);
                    Path path = Paths.get("QR.jpg");
                    MatrixToImageWriter.writeToPath(matrix, "JPG", path);
                    
                    ImageIcon icon  = new ImageIcon("QR.jpg");
                    lblQR.setIcon(icon);
                    lblQR.repaint();
                }
                catch(Exception e){
                    e.printStackTrace();
                }
            }       
        
                   
    
    
