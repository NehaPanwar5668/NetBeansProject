             void insertData()
             {
                   SimpleDateFormat s1 = new SimpleDateFormat("yyyy-MM-dd");
                 String date1=s1.format(DateChooser);
                try
                {
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    String url = "jdbc:mysql://localhost:3306/feesmsdatabase?zeroDateTimeBehavior=CONVERT_TO_NULL ";
                    Connection con=DriverManager.getConnection(url, "root", "oracle");
                    
                    String sql = "insert into collect_fees (student_name,roll_no,course,semester,payment_mode,remark,payment_date,fees_amount) values(?,?,?,?,?,?,?,?)";
                    PreparedStatement st = con.prepareStatement(sql);
//                    st.setInt(1, getreceipt());
                    st.setString(1, txtSname);
                    st.setString(2, txtRnumber);
                    st.setString(3, cmbCourse);
                    st.setString(4, cmbSemester);
                    st.setString(5, cmbPaymode);
                    st.setString(6, txtTextarea);
                    st.setString(7,date1 );
                    st.setString(8, txtFamount);
                     
                   
                   int i =st.executeUpdate();
                   if(i>0)
                   {
                       JOptionPane.showMessageDialog(this, "Fees collected successfully");
                   }
                   else
                   {
                     JOptionPane.showMessageDialog(this, "Fees not collected successfully");  
                   }
                   
                }
                catch(Exception e)
                {
            e.printStackTrace();
                } 
                
             }
             boolean validation()
             {
               txtSname =  jtxtSname.getText();
               txtRnumber =  jtxtRnumber.getText();
               cmbCourse =  jcmbCourse.getSelectedItem().toString();
               cmbSemester=  jcmbSemester.getSelectedItem().toString();
               txtFamount =  jtxtFamount.getText();
               txtTextarea =  jtxtTextarea.getText();
               cmbPaymode = jcmbPaymode.getSelectedItem().toString();
               DateChooser= jDateChooser.getDate();
               
               
               if(txtSname.equals(""))
               {
                   JOptionPane.showMessageDialog(this,"Please! enter your  name");
                   return false;
               }
                if(txtRnumber.equals(""))
               {
                   JOptionPane.showMessageDialog(this,"Please! enter your roll number");
                   return false;
               }
                 if(cmbCourse.equals(""))
               {
                   JOptionPane.showMessageDialog(this,"Please! select your course");
                   return false;
               }
                  if(cmbSemester.equals(""))
               {
                   JOptionPane.showMessageDialog(this,"Please! select your semester");
                   return false;
               }
                    if(cmbPaymode.equals(""))
               {
                   JOptionPane.showMessageDialog(this,"Please! select your payment method");
                   return false;
               }
                    if(txtTextarea.equals(""))
                    {
                    JOptionPane.showMessageDialog(this,"Please! this feild is required");
                   return false;
                    }
                     if(DateChooser== null)
               {
                   JOptionPane.showMessageDialog(this,"Please! enter the Date");
                   return false;
               }
                     return true;
             }
             
